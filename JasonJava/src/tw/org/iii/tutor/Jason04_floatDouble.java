package tw.org.iii.tutor;

public class Jason04_floatDouble {

	public static void main(String[] args) {
		// float 2^32; double 2 ^64
		float var1 = 123;
		float var2 = 12.3f;
		double var3 = 123;
		double var4 = 12.3;
		double var5 = 10 / 3.0;
		System.out.println(var5);
		float var6 = 10 / 3.0f;
		System.out.println(var6);
				
	}

}
